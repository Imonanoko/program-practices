.MODEL SMALL
.DATA
STUDENTID DB 's10350136',0AH,0DH
STUDENTNAME DB 'GUAN-WEI CHEN',0AH,0DH,'$'
IsEven DB 'I have an even student number. $'
IsOdd DB 'I have an odd student number. $'
.CODE
MAIN PROC
A10:
	MOV AX,@DATA
	MOV DS,AX
	;MOV ES,AX
	LEA SI,STUDENTID
	LEA DI,STUDENTNAME+1
	MOV AX,[SI]
	XOR AX,20H
	MOV [SI],AX
	MOV CX,000CH
A40:
	MOV AL,[DI]
	CMP AL,41H
	JB A60
	CMP AL,5AH
	JA  A60
	XOR AL,20H
	MOV [DI],AL
	INC DI
	LOOP A40
A60:
	ADD DI,2
	Loop A40
A50:
	LEA DX,STUDENTID
	MOV AH,09H
	INT 21H
	;判斷奇偶
	MOV AX,[SI+8]
	AND AX,000FH
	MOV BL,02H
	DIV BL
	CMP AH,00H
	JE  A20
	JA  A30
	
	
A20:
	LEA DX,ISEVEN
	MOV AH,09H
	INT 21H
	MOV AX,4C00H
	INT 21H
A30:
	LEA DX,ISODD
	MOV AH,09H
	INT 21H
	MOV AX,4C00H
	INT 21H



MAIN ENDP
END MAIN